// If Statement
let myAgeNow = 15;
if (myAgeNow >= 18) {
	console.log("You are an adult");
}

// Else if Statement
let num = 0;
if (num > 0) {
	console.log("This is positive Number");
} else if (num < 0) {
	console.log("This is negative Number");
} else {
	console.log("This is zero");
}
