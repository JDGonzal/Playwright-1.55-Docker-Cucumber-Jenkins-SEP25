// For Loop
for (let i = 0; i < 5; i++) {
	console.log(i);
}

// While Loop
let count = 0;
while (count < 5) {
	console.log(count);
	count++;
}

// Do While Loop
let num = 0;
do {
	console.log(num);
	num++;
} while (num < 5);

// For Loop and break
for (let i = 0; i < 10; i++) {
	if (i === 5) {
		break;
	}
	console.log(i);
}
